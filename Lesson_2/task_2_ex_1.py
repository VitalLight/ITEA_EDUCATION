""""
Ввести 2 числа: перше конвертувати у тип float, а друге у тип -int. Вивести суму чисел.
"""
try:
    a1 = float(input("Введіть число типу float(з крапкою):  "))
    a2 = int(input("Введіть число типу int:   "))
except ValueError:
    print("\n ВВедене значення числа а1 чи а2 не відповідає типу")
    exit()

s = a1 + a2
print("\n Сума чисел а1+а2 становить {}".format(s))