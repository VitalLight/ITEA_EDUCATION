""""
Написати програму для обчислення площі трикутника за висотою та стороною. Результати вивести на екран.
"""
try:
    a = float(input("Введіть довжину основи трикутника a:  "))
    h = float(input("Введіть висоту трикутника до цієї основи h   :"))
except ValueError:
    print("\n ВВедене значення довжини основи чи висоти трикутника до цієї основи НЕ числове значення")
    exit()
# площа трикутника становить s = a * (h/2)
s = a * (h/2)
print("\n Площа трикутника становить {}".format(s))