""""
Користувач вводить число n. Вивести на екран різницю добутків чисел n+1 та n-1.
"""
print("Введіть число n >0")
try:
    n = float(input("Введіть число n:   "))
    if n <= 0:
        print("Коефіцієнт n  менший або рівний НУЛЮ. ")
        exit()
except ValueError:
    print("Введені не числові значення. Повторіть спочатку")
    exit()

print ("Різниця добутків (n+1)**2 - (n-1)**2 = {}".format ((n + 1)**2 - (n - 1)**2))